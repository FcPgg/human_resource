package com.xt.ptus.mt.pojo;

public class MtOrderSellerPojo {
	private String id;
	
	private String sellerName;
	
	private int orderId;
	
	private String logo;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSellerName() {
		return sellerName;
	}

	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}

	public int getOrderId() {
		return orderId;
	}

	public void setOrderId(int orderId) {
		this.orderId = orderId;
	}

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public MtOrderSellerPojo(String id, String sellerName, int orderId, String logo) {
		super();
		this.id = id;
		this.sellerName = sellerName;
		this.orderId = orderId;
		this.logo = logo;
	}
	
}
